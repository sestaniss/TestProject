package class8;

public class NestedLoops6 {
    public static void main(String[] args) {

        for (int i=0; i < 3; i++) { // executes the below code 3 times

            for (int j = 1; j < 6; j++) { // prints the value of j from 1 to 5

                System.out.print(j);

            }

            System.out.println();

        }

    }
}
